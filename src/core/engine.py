import re
from typing import List, Set

class ScoringEngine:
    """
    Класс для оценки релевантности предложений нормативным требованиям.

    Система оценки предложений основана на трех факторах:
    1. Анализ с помощью списка глаголов долженствования;
    2. Исключение бюрократического мусора;
    3. Поиск физических величин (цифры + единицы измерения).
    """

    def __init__(self):
        """
        Инициализация набора ключевых паттернов и черных списков.
        """

        # Строгая модальность
        self._modal_keywords: Set[str] = {
            'должен', 'должны', 'должна', 'следует', 'необходимо', 'не допускается',
            'запрещается', 'не менее', 'не более', 'предел', 'погрешность', 'допускается'
        }

        # Стоп шаблоны
        self._black_list: Set[str] = {
            'сведения о стандарте', 'введен в действие', 'принят',
            'голосовал', 'краткое наименование', 'код страны',
            'национальный орган', 'разработан', 'внесен', 'институт',
            'впервые', 'переиздан', 'дата введения', 'предислови',
            'рис', 'рисунок', 'таблица', 'табл.', 'проверка расхода',
            'измеряют расходомером', 'после испытаний', 'при испытани',
            'метод испытаний', 'правила приемки', 'технические требования',
            'условия проведения', 'аппаратура и материалы', 'подготовка к испытанию',
            'обработка результатов', 'не должно иметь видимых', 'согласно',
            'приложен', 'сокращен', 'формул', 'приведен', 'термины', 'соответствующими определениями', 'настоящий стандарт', 'область применения', 'раздел'
            }

        # Главные сущности - поиск диапазона (0,2..0,8),
        # допуски (±), единицы измерения
        self._metrics_regex = re.compile(
            r'(\d+([.,]\d+)?\s*(±|..|—|-|до)\s*\d+([.,]\d+)?|\d+([.,]\d+)?)\s*'
            r'(л/с|кг|м|см|мм|%|МПа|°C|мин|с|В|Гц|кВт|Па|кДж|Вт/м|ед|шт)',
            re.IGNORECASE
        )

    def calculate_score(self, sentence: str) -> float:
        """
        Рассчитывает score релевантности для переданного предложения.

        Args:
            sentence (str): Предложение для анализа.

        Returns:
            float: Итоговый score. Отрицательные значения указывают на шум.

        Description:
            - Черный список: -5.0
            - Оценка модальности: +2.0
            - Наличие физических величин: +3.0
            - Бонус за допуски: +1.5
            - Слишком длинные (>600) или короткие (<35): -2.0

            Максимальный вес получают предложения, содержащие модальность,
            и числовые параметры.
        """

        score: float = 0.0
        sent_lower = sentence.lower()

        # Жесткий фильтр черного списка
        if any(bad_word in sent_lower for bad_word in self._black_list):
            return -5.0 # Глубокий минус для бюрократии

        # Оценка модальности
        if any(word in sent_lower for word in self._modal_keywords):
            score += 2.0

        # Оценка наличия физических величин
        if self._metrics_regex.search(sentence):
            score += 3.0

        # Бонус за допуски
        if any(sym in sentence for sym in ['±', '+/-', '>', '<', '≥', '≤']):
            score += 1.5

        # Пенальти за длину, признак неструктурированного текста
        if len(sentence) > 600 or len(sentence) < 35:
            score -= 2.0

        return score

    def has_metrics(self, sentence: str) -> bool:
        """
        Проверяет наличие физических величин в предложении.

        Args:
            sentence (str): Предложение для анализа.

        Returns:
            bool: True, если найдены числовые параметры.
        """

        return bool(self._metrics_regex.search(sentence))

    def extract(self, sentences: list, threshold: float = 2.0) -> list:
        """
        Отбирает предложения с весом выше порогового значения.

        Args:
            sentences: Список предложений для анализа
            threshold: Минимальный вес для включения в результат (по умолчанию 2.0)

        Returns:
            Отсортированный по убыванию веса список кортежей: предложение, вес

        Порог 2.0 выбран эмпирически:
            - <2.0: слишком много ложных срабатываний
            - >2.0: риск пропустить требования без числовых значений
        """

        found = []
        for sent in sentences:
            score = self.calculate_score(sent)
            if score >= threshold:
                found.append((sent.strip(), score))

        return sorted(found, key=lambda x: x[1], reverse=True)
