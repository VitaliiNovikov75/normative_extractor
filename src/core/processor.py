# processor.py
import re
from typing import List

class TextProcessor:
    """
    Класс для предварительной обработки и сегментации нормативных текстов.

    Отвечает за очистку исходного текста от технического шума сайта и
    корректное разбиение массива данных на предложения.
    """

    def __init__(self):
        """
        Инициализация регулярных выражений для обработки текста.
        """

        # Паттерн учитывает сокращения и не дает делить предложения на них (ГОСТ Р, п., табл.)
        self._split_regex = re.compile(
            r'(?<!\bтабл)(?<!\bсм)(?<!\bрис)(?<!\bп)(?<!\bг)(?<!\bГОСТ)(?<!\bР)\.\s+(?=[А-Я])'
        )
        # Паттерн для поиска заголовка ГОСТ/СП в начале текста
        self._name_regex = re.compile(r'((?:ГОСТ|СП|ГОСТ Р)\s+[\d\s\-\:]+)')

    def extract_document_name(self, text: str) -> str:
        """
        Динамически извлекает официальное наименование документа из текста.

        Args:
            text (str): Очищенный текст документа.

        Returns:
            str: Найденное название или 'Unknown Document', если имя не определено.
        """

        # Паттерн названия в первых 5000 символах текста
        match = self._name_regex.search(text[:5000])
        if match:
            return match.group(1).strip()
        return 'Unknown Document'

    def clean_garbage(self, raw_text: str) -> str:
        """
        Удаляет навигационный шум сайта и нормализует структуру текста.

        Args:
            raw_text (str): Сырой текст, полученный от загрузчика.

        Returns:
            str: Текст, очищенный от шапки и лишних пробелов.

        Description:
            Ищет первое вхождение маркеров начала стандарта:
            - 1 Область
            - 1. ОБЛАСТЬ
            - Область применения

            Если маркер найден, возвращается текст от этой позиции до конца.
            Если не найден, возвращается исходный текст без изменений.
        """

        # Отрезает все, что находится до раздела Область применения
        match = re.search(
            r'(1\s+Область|1\.\s+ОБЛАСТЬ|Область применения)',
            raw_text, re.IGNORECASE | re.MULTILINE
            )

        content = raw_text[match.start():] if match else raw_text

        # Нормализация пробелов и переносов строк в единый поток
        normalized = re.sub(r'\s+', ' ', content)
        return normalized.strip()

    def split_to_sentences(self, clean_text: str) -> List[str]:
        """Разбивает текст на отдельные предложения.

        Args:
            text: Очищенный текст документа

        Returns:
            Список предложений длиной более 35 символов

        Алгоритм:
            1. Замена переносов строк на пробелы (нормализация)
            2. Сжатие множественных пробелов в один
            3. Разделение по паттерну sentence_end (точка + пробел + заглавная)
            4. Добавление точки в конец каждого предложения
            5. Фильтрация коротких фрагментов

            Паттерн игнорирует точки после распространенных сокращений,
            что предотвращает ложное разбиение внутри предложения.
        """

        # Сегментация, устойчивая к техническим сокращениям
        sentences = self._split_regex.split(clean_text)

        # Фильтрация пустых строк и слишком коротких фрагментов
        return [s.strip() + '.' for s in sentences if len(s.strip()) > 35]
